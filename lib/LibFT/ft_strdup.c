#include "libft.h"

char	*ft_strdup(const char *s)
{
	char			*dup;
	unsigned int	i;
	unsigned int	len;

	i = 0;
	len = ft_strlen(s);
	dup = (char *)ft_malloc(len + 1);
	if (!dup)
		return (0);
	while (s[i])
	{
		dup[i] = s[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
